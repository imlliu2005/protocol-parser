#include <cstdint>
namespace medex 
{
    namespace hut
    {
        // 握手命令
        // 5A 05 82 E2 D2
        uint8_t hand_shake_cmd[] = {0x5A, 0x05, 0x82, 0xE2, 0xD2};

        // 取得设备记录条数
        // 0x5A 0x05 0x53 crc16
        uint8_t get_record_count_cmd[] = {0x5A, 0x05, 0x53, 0xBE, 0x12};

        // 读取得指定记录数据命令
        // 比如要读取记录号为 1 的数据记录:
        // 0x5A 0x07 0x54 0x00 0x01 crc16
        // 5A 07 54 00 01 69 FD
        uint8_t get_specified_record_cmd[] = {0x5A, 0x07, 0x54, 0x00, 0x01, 0x69, 0xFD};

        // 设置 5 分钟后开始测量
        // 主机发送设置 5 分钟后是否启动测量----0x01:启用 0x00:禁用
        // 0x5A 0x06 0x44 0x01 crc16
        // 0x01:启用
        uint8_t set_auto_5min_measure_cmd[] = {0x5A, 0x06, 0x44, 0x01, 0x3D, 0x00};
        // 0x00:禁用
        uint8_t set_disable_auto_5min_measure_cmd[] = {0x5A, 0x06, 0x44, 0x00, 0xFD, 0xC1};

        // 设备内记录清 0：
        // A：主机发送设备内记录清零命令：
        // 0x5A 0x05 0x57 crc16
        // B：下位机返回：
        // 成功: 0x5A 0x07 0x57 0x00 0x00 crc16
        uint8_t clean_device_records_cmd[] = {0x5A, 0x07, 0x57, 0x00, 0x00,0xA9, 0xCC}; 
    }// hut
}// medex
